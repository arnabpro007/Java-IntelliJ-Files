public class Switch {
    public static void main(String[] args) {
        int number = 2 ;
        switch (number){
            case 1:
                System.out.println("This is interesting!");
                break;
            case 7:
                System.out.println("This is not interesting!");
                break;
            default:
                System.out.println("Y'all going to hell!");
        }
    }

}
